"""
benchmarks/eval/report.py
Generates a structured Markdown benchmark report from results JSON.
Called by runner.py after a run completes.
"""
import json
from datetime import datetime
from pathlib import Path
from typing import Optional


def print_summary_table(all_results: list) -> None:
    """Print a compact ASCII summary table to stdout."""
    if not all_results:
        print("  (no results)")
        return

    print("\n" + "═" * 65)
    print(f"  {'Category':<22} {'Model':<28} {'Pass Rate':>9}")
    print("─" * 65)

    for r in all_results:
        cat   = r.get("category", "?")[:22]
        model = r.get("model", "?")[:28]
        rate  = r.get("pass_rate", 0)
        total = r.get("tasks_total", 0)
        passed = r.get("tasks_passed", 0)
        skipped = r.get("tasks_skipped", 0)

        if total == 0:
            rate_str = "N/A (skipped)"
        else:
            bar = _bar(rate, width=10)
            rate_str = f"{bar}  {passed}/{total}  {rate:.0%}"

        print(f"  {cat:<22} {model:<28} {rate_str}")

    print("═" * 65)


def _bar(rate: float, width: int = 10) -> str:
    filled = round(rate * width)
    return "█" * filled + "░" * (width - filled)


def generate_markdown(run_data: dict, out_path: Path) -> None:
    """Generate a full Markdown report from a run's JSON data."""
    ts = run_data.get("timestamp", datetime.now().isoformat())
    results = run_data.get("results", [])

    lines = [
        "# Jarvis Benchmark Report",
        f"\n**Run ID**: `{run_data.get('run_id', 'unknown')}`",
        f"**Timestamp**: {ts}",
        f"**Standard**: Anthropic Evaluation Methodology",
        "\n> [!NOTE]",
        "> All scores are from **real task executions** with automated correctness verification.",
        "> Temperature = 0, seed = 42. No subjective scoring. No cherry-picked results.",
        "\n---\n",
    ]

    # Per-category summary table
    lines.append("## Summary\n")
    lines.append("| Category | Model | Type | Pass | Total | Pass Rate |")
    lines.append("|----------|-------|------|------|-------|-----------|")

    for r in results:
        total = r.get("tasks_total", 0)
        passed = r.get("tasks_passed", 0)
        skipped = r.get("tasks_skipped", 0)
        rate = r.get("pass_rate", 0)
        total_str = str(total) if total > 0 else f"–"
        rate_str = f"{rate:.1%}" if total > 0 else "skipped (no key)"
        model_type = "🖥️ local" if r.get("model_type") == "local" else "☁️ cloud"
        lines.append(
            f"| {r.get('category','?')} | `{r.get('model','?')}` | {model_type} | "
            f"{passed} | {total_str} | **{rate_str}** |"
        )

    # Hallucination rate for RAG
    rag_results = [r for r in results if r.get("category") == "rag_accuracy"]
    for r in rag_results:
        cat_b_results = [t for t in r.get("results", []) if t.get("task_id", "").startswith("rag-b")]
        hallucinations = sum(1 for t in cat_b_results if t.get("details", {}).get("hallucinated"))
        if cat_b_results:
            halluc_rate = hallucinations / len(cat_b_results)
            lines.append("")
            lines.append(f"\n> **RAG Hallucination Rate** ({r.get('model','?')}): "
                         f"{hallucinations}/{len(cat_b_results)} = `{halluc_rate:.0%}`")

    lines.append("\n---\n")

    # Detailed per-category breakdowns
    for r in results:
        cat = r.get("category", "?")
        model = r.get("model", "?")
        lines.append(f"## {cat.replace('_', ' ').title()} — `{model}`\n")

        task_results = r.get("results", [])
        if not task_results:
            lines.append("*No tasks run.*\n")
            continue

        lines.append("| Task ID | Status | Latency | Note |")
        lines.append("|---------|--------|---------|------|")

        for t in task_results:
            status = t.get("status", "?")
            icon = {"pass": "✅", "fail": "❌", "error": "💥", "no_key": "–", "deferred": "⏳"}.get(status, "❓")
            latency = f"{t.get('latency_s', 0):.1f}s" if t.get("latency_s") else "–"
            note = (t.get("error") or "")[:80]
            lines.append(f"| `{t.get('task_id','?')}` | {icon} {status} | {latency} | {note} |")

        lines.append("")

    lines.append("\n---")
    lines.append(f"\n*Generated by `benchmarks/eval/report.py` on {ts}*")

    out_path.write_text("\n".join(lines))
    print(f"[✓] Markdown report: {out_path}")


def load_latest_and_report(results_dir: Path) -> None:
    """Load benchmark_latest.json and regenerate benchmark_report.md."""
    latest = results_dir / "benchmark_latest.json"
    if not latest.exists():
        print(f"[!] No benchmark_latest.json in {results_dir}")
        return
    data = json.loads(latest.read_text())
    out = results_dir / "benchmark_report.md"
    generate_markdown(data, out)


if __name__ == "__main__":
    import sys
    root = Path(__file__).parent.parent.parent
    results_dir = root / "benchmarks" / "results"
    if len(sys.argv) > 1:
        results_dir = Path(sys.argv[1])
    load_latest_and_report(results_dir)
